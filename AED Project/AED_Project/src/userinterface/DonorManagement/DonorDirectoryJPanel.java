    public void populateAppointmentTable() {
        
        DefaultTableModel dTM = (DefaultTableModel) receptionistAppointmentTable2.getModel();
        dTM.setRowCount(0);

        for (Donor d : donorDirectory.getDonorDirectory()) {

            Object row[] = new Object[3];
            row[0] = d;
            row[1] = d.getFirstName();
            row[2] = d.getMobileNo();

            dTM.addRow(row);
        }
    }

    public void scanDonor() {
        String csvFile = "/Users/rajusingh/NetBeansProjects/aed_fall_2016_project_sameer_suman_001289666/MyDatabank/Data for array.txt";
        BufferedReader br = null;
        String line = "";
        String cvsSplitBy = ",";
        try {
            br = new BufferedReader(new FileReader(csvFile));
        } catch (FileNotFoundException e) {
            // TODO Auto-generated catch block
            e.printStackTrace();
        }
        try {
            while ((line = br.readLine()) != null) {

                // use comma as separator
                String[] country = line.split(cvsSplitBy);
                Donor d = donorDirectory.addDonor();
                d.setLastName(country[0]);
                d.setFirstName(country[1]);
                d.setMiddleName(country[2]);
                d.setMobileNo(country[3]);
            }

        } catch (IOException e) {
            // TODO Auto-generated catch block
            e.printStackTrace();
        }
    }

    private void Search(String s) {
        TableModel vT = receptionistAppointmentTable2.getModel();
        final TableRowSorter<TableModel> sorter = new TableRowSorter<TableModel>(vT);
        receptionistAppointmentTable2.setRowSorter(sorter);
        String text = searchTxtField.getText();
        if (text.length() == 0) {
            sorter.setRowFilter(null);
            receptionistAppointmentTable2.clearSelection();
        } else {
            try {
                sorter.setRowFilter(RowFilter.regexFilter("(?i)" + text));
            } catch (PatternSyntaxException pse) {
                pse.printStackTrace();
            }
        }

    }
