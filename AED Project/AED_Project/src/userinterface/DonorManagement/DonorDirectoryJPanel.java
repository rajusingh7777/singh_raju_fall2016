    private void viewDonorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewDonorActionPerformed

        int selectedRow = receptionistAppointmentTable2.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select any row to get details", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            // Setting value in corresponding fields
            // Donor Profile

            Donor d = (Donor) receptionistAppointmentTable2.getValueAt(selectedRow, 0);
            lastNameTxtField.setText(d.getLastName());
            firstNameTxtField.setText(d.getFirstName());
            middleNameTextField.setText(d.getMiddleName());
            nationIDTxtField.setText(d.getNationID());
            streetAddTextArea.setText(d.getStreetAddress());
            zipPostalTxtField.setText(d.getZipPostal());
            cityTxtField.setText(d.getCity());
            stateTxtField.setText(d.getStateProvince());
            countryTxtField.setText(d.getCountry());
            emailTxtField.setText(d.getEmailID());
            mobileTxtField.setText(d.getMobileNo());
            occupationTextField.setText(d.getOccupation());
            donorPresenceStatusTxtField.setText(d.getDonorPresenceStatus());
            donationStatusTxtField.setText(d.getDonationStatus());

            //Nurse Assesment
            majorAlignmentTxtField.setText(d.getAnyMajorAlignmentWhat());
            bloodPressureTxtField.setText(d.getBloodPressure());
            heightTxtField.setText(d.getHeight());
            weightTxtField.setText(d.getWeight());
            deferredWhyTxtField.setText(d.getDeferredWhy());

            //LabReport
            donorFutureEligbilityifNoWhyTxtField.setText(d.getDonorFutureEligbilityifNoWhy());

        }
    }//GEN-LAST:event_viewDonorActionPerformed

